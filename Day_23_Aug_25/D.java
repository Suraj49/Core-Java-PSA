package Day_23_Aug_25;

//for loop:- when we want to do any work again and again then we use concepts of loop

public class D {
    public static void main(String[] args) {
        for (int i=0;i<10;i++){ //0 to 9 total no of 10 times loop will execute
            System.out.println(i);
            System.out.println("Suraj");
        }
    }
}
